<script>
import moment from "moment";
export default {
  name: "Blogs",
  data() {
    return {
      scrollVal: 300,
    };
  },
  methods: {
    scrollNext() {
      this.validateScroll();
      this.documentScroll("+");
      this.scrollTo();
    },
    scrollBack() {
      this.validateScroll();
      this.documentScroll("-");
      this.scrollTo();
    },
    documentScroll(operator) {
      const scrollWith = 300;
      operator === "+"
        ? (this.scrollVal += scrollWith)
        : (this.scrollVal -= scrollWith);
    },
    scrollTo() {
      const el = document.getElementById("popularComparisons");
      el.scrollTo({
        left: this.scrollVal,
        behavior: "smooth",
      });
    },
    validateScroll() {
      if (this.scrollVal <= 0) {
        this.scrollVal = 300;
      }
      if (this.scrollVal >= 2400) {
        this.scrollVal = 2400;
      }
    },
  },
};
</script>

