<script>
import JetApplicationLogo from "@/Jetstream/ApplicationLogo.vue";
import JetDropdown from "@/Jetstream/Dropdown.vue";
import JetDropdownLink from "@/Jetstream/DropdownLink.vue";
import CityModal from "./CityModal.vue";

export default {
  components: {
    JetApplicationLogo,
    JetDropdown,
    JetDropdownLink,
    CityModal,
  },
  data() {
    return {
      isMenuOpen: false,
      selectCity: false,
      cityName: localStorage.getItem("citySelectedName") ?? "Bangalore",
      query: null,
      errorMessage: null,
      bikes: [],
      bikeHovered: null,
      isMobile: false,
    };
  },
  beforeMount() {
    const citySelectedName = localStorage.getItem("citySelectedName");
    const citySelectedId = localStorage.getItem("citySelectedId");

    if (!citySelectedName) {
      localStorage.setItem("citySelectedName", "Bangalore");
    }
    if (!citySelectedId) {
      localStorage.setItem("citySelectedId", 1);
    }
  },
  mounted() {
    var emitter = require("tiny-emitter/instance");

    emitter.on("city-selected", (val) => {
      this.cityName = val.city_name;
    });

    document.addEventListener("click", (evt) => {
      const flyoutElement = document.getElementById("search-list");
      let targetElement = evt.target; // clicked element

      do {
        if (targetElement == flyoutElement) {
          // This is a click inside. Do nothing, just return.
          console.log("click inside");
          return;
        }
        // Go up the DOM
        targetElement = targetElement.parentNode;
      } while (targetElement);

      // This is a click outside.
      this.bikes = [];
      this.query = null;
    });
  },
  methods: {
    citySelected() {
      this.cityName = localStorage.getItem("citySelectedName");
    },
    search(val) {
      if (val === "mobile") {
        this.isMobile = true;
      } else {
        this.isMobile = false;
      }
      if (this.query.length >= 3) {
        this.errorMessage = null;
        axios
          .post("/search", {
            query: this.query,
          })
          .then((res) => {
            this.bikes = res.data;
          })
          .catch((err) => {
            console.log(err);
          });
      } else {
        this.errorMessage = "Please type 3 or more letters";
      }
    },
    isHovered(id) {
      this.bikeHovered = id;
    },
    slugify(text) {
      return text
        .toLowerCase()
        .replace(/ /g, "-")
        .replace(/[^\w-]+/g, "");
    },
    logout() {
      this.$inertia.post(route("logout"));
    },
  },
};
</script>
